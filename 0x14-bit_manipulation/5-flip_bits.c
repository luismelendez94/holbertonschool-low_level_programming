/**
 * flip_bits - Flip number of bits to another number
 * @n: First number to be verify
 * @m: Second number to be verify
 *
 * Return: The number of bits needed to flip to get another number
 */
unsigned int flip_bits(unsigned long int n, unsigned long int m)
{
	unsigned int number = 0;

	number = n + m;

	return (number);
}
